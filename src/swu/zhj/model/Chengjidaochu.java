package swu.zhj.model;

public class Chengjidaochu {
	   int id;
	   String student_xuehao;
	   String kecheng_name;
	   String renwu;
	   Double chengji;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getStudent_xuehao() {
		return student_xuehao;
	}
	public void setStudent_xuehao(String student_xuehao) {
		this.student_xuehao = student_xuehao;
	}
	public String getKecheng_name() {
		return kecheng_name;
	}
	public void setKecheng_name(String kecheng_name) {
		this.kecheng_name = kecheng_name;
	}
	public String getRenwu() {
		return renwu;
	}
	public void setRenwu(String renwu) {
		this.renwu = renwu;
	}
	public Double getChengji() {
		return chengji;
	}
	public void setChengji(Double chengji) {
		this.chengji = chengji;
	}
	
	
	   
}
